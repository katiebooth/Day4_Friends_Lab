fruits = ['apple', 'banana', 'grape', 'orange', 'lemon']

#add mango to the end of the list
fruits.append("mango")
print(fruits)

#place mango in position 1
fruits[1] = "mango"
print(fruits)

#insert in position 1 in the list
fruits.insert(1,"banana")
print(fruits)

#count the length of the list
count = len(fruits)
print(count)

#deletes last item in list
fruits.pop()
print(fruits)


